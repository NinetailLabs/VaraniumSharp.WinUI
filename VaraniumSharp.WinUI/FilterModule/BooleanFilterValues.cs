namespace VaraniumSharp.WinUI.FilterModule
{
    /// <summary>
    /// Values for a bool filter
    /// </summary>
    public enum BooleanFilterValues
    {
        /// <summary>
        /// Ignore the filter, show all values
        /// </summary>
        All,

        /// <summary>
        /// Filter for only true values
        /// </summary>
        Yes,

        /// <summary>
        /// Filter only for false values
        /// </summary>
        No
    }
}